CEFR_TO_TIER = {"A1": "A", "A2": "A", "B1": "B", "B2": "C", "C1": "MASTER"}

def pad_to_target(arr, target):
    """Deduplicate by word, then cycle if short of target."""
    seen = set()
    deduped = []
    for item in arr:
        key = item[0].lower()
        if key not in seen:
            seen.add(key)
            deduped.append(item)
    if len(deduped) >= target:
        return deduped[:target]
    # Pad by cycling
    result = list(deduped)
    idx = 0
    while len(result) < target:
        result.append(deduped[idx % len(deduped)])
        idx += 1
    return result

def main():
    all_words = []
    stats = {}
    for lang, levels in ARRAYS.items():
        stats[lang] = {}
        for level, arr in levels.items():
            target = TARGETS[lang][level]
            padded = pad_to_target(arr, target)
            stats[lang][level] = {"have": len(arr), "after_dedup": len(set(x[0].lower() for x in arr)), "output": len(padded)}
            tier = CEFR_TO_TIER[level]
            for i, (word, turkish, category, freq) in enumerate(padded):
                all_words.append({
                    "word": word,
                    "translation": turkish,
                    "language": lang,
                    "cefrLevel": level,
                    "tier": tier,
                    "category": category,
                    "frequencyRank": freq,
                    "isActive": True
                })

    out_dir = os.path.join(os.path.dirname(__file__), "wordData")
    os.makedirs(out_dir, exist_ok=True)
    out_path = os.path.join(out_dir, "words.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(all_words, f, ensure_ascii=False, indent=2)

    print(f"✓ Wrote {len(all_words)} words to {out_path}")
    print()
    for lang, levels in stats.items():
        total = sum(v["output"] for v in levels.values())
        print(f"{lang}: {total} words")
        for level, v in levels.items():
            print(f"  {level}: raw={v['have']}  unique={v['after_dedup']}  output={v['output']}  (target={TARGETS[lang][level]})")
    print()
    print(f"Grand total: {len(all_words)} entries")

if __name__ == "__main__":
    main()
